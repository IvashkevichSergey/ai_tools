"""Инструмент для стратегической рефлексии и планирования.

ThinkTool позволяет агенту делать паузу для анализа прогресса и принятия
решений о следующих шагах, что улучшает качество работы над сложными задачами.
"""

from langchain_core.tools import tool


@tool(parse_docstring=True)
def think_tool(reflection: str) -> str:
    """Инструмент для стратегической рефлексии о прогрессе исследования и принятии решений.

    Используй этот инструмент после каждого поиска для анализа результатов и планирования
    следующих шагов. Это создаёт преднамеренную паузу в рабочем процессе для качественного
    принятия решений.

    Для структурных запросов по документу использовать такой порядок:
    1) Сначала найти оглавление/содержание;
    2) Определить целевой пункт по порядку или названию;
    3) Только затем искать факты по соответствующему пункту.

    Когда использовать:
    - После получения результатов поиска: Какую ключевую информацию я нашёл?
    - Перед принятием решения о следующих шагах: Достаточно ли у меня информации для полного ответа?
    - При оценке пробелов в исследовании: Какой конкретной информации мне всё ещё не хватает?
    - Перед завершением исследования: Могу ли я предоставить полный ответ сейчас?

    Рефлексия должна включать:
    1. Анализ текущих находок - Какую конкретную информацию я собрал?
    2. Оценка пробелов - Какая важная информация всё ещё отсутствует?
    3. Оценка качества - Достаточно ли у меня доказательств/примеров для хорошего ответа?
    4. Стратегическое решение - Продолжать ли поиск или предоставить ответ?

    Args:
        reflection: Твоя детальная рефлексия о прогрессе исследования, находках, пробелах и следующих шагах

    Returns:
        Подтверждение, что рефлексия записана для принятия решений
    """
    return f"Reflection recorded: {reflection}"

