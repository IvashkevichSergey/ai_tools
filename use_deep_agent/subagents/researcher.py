"""Подагент для поиска фактов в документах."""

import os
from importlib.resources import files

from dotenv import load_dotenv
from langchain_gigachat import GigaChat

from research_agent.tools.document_tool import read_document_pages, search_document

load_dotenv(str(files("research_agent").joinpath("config", "demo_env.env")))

llm = GigaChat(
    verify_ssl_certs=False,
    model=os.getenv("GIGACHAT_MODEL", "GigaChat-2-Max"),
    timeout=600,
    profanity_check=False,
    temperature=0.1,
    max_tokens=1200,
)

RESEARCHER_INSTRUCTIONS = """Роль: провести точечный поиск по документу и вернуть проверяемую фактуру.

Цель:
- собрать релевантные данные по делегированной теме;
- отделить факты от интерпретаций;
- вернуть материал, пригодный для последующего черновика письма.

Правила выполнения:
- Работать только через search_document.
- Если нужно понять структуру документа или посмотреть его начало, использовать read_document_pages.
- Формулировать 1-3 уточняющих поисковых запроса, если первого запроса недостаточно.
- Если пользователь ссылается на раздел по номеру или порядку, сначала определи заголовок нужного раздела по структуре документа через read_document_pages, затем ищи факты уже по этому заголовку.
- Под первым разделом понимать первый содержательный раздел документа, а не обложку, оглавление или введение, если пользователь явно не просит именно их.
- Документ уже загружен в систему. Не запрашивать у пользователя файл повторно.
- Не добавлять сведения, которых нет в найденных фрагментах.
- Не пересказывать весь документ, держать фокус на задаче.

Критерии качества:
- факты соответствуют запросу;
- нет противоречий между пунктами;
- термины и формулировки по возможности совпадают с документом;
- отмечены пробелы, если данных недостаточно.

Формат ответа:
1) Короткий заголовок (1 строка).
2) Факты (3-8 пунктов, каждый пункт 1-2 предложения).
3) Пробелы/ограничения (если есть, 1-3 пункта).
4) Вывод (1-2 предложения, что можно утверждать уверенно).
"""


def create_research_agent() -> dict:
    """Создаёт конфигурацию подагента для поиска в документе."""
    return {
        "name": "researcher",
        "description": "Ищет и структурирует факты из документа по одной теме, фиксирует пробелы и даёт сжатый доказательный вывод.",
        "system_prompt": RESEARCHER_INSTRUCTIONS,
        "tools": [search_document, read_document_pages],
        "model": llm,
    }
