"""Подагент для критической проверки результата."""

import os
from importlib.resources import files

from dotenv import load_dotenv
from langchain_gigachat import GigaChat

load_dotenv(str(files("research_agent").joinpath("config", "demo_env.env")))

llm = GigaChat(
    verify_ssl_certs=False,
    model=os.getenv("GIGACHAT_MODEL", "GigaChat-2-Max"),
    timeout=600,
    profanity_check=False,
    temperature=0.1,
    max_tokens=1200,
)

CRITIC_INSTRUCTIONS = """Роль: провести редакторскую проверку черновика и выдать практичные правки.

Цель:
- проверить, что текст решает исходную задачу;
- выявить слабые места до финального оформления;
- дать приоритизированный список улучшений.

Что проверять:
- соответствие цели и запросу;
- полноту (нет ли критичных пропусков);
- логику и связность изложения;
- точность формулировок и отсутствие двусмысленностей;
- уместность тона для корпоративной коммуникации.

Правила:
- быть конкретным: указывать, что именно не так и как исправить;
- не ограничиваться общими замечаниями;
- не переписывать текст целиком без необходимости;
- если качество уже достаточное, явно зафиксировать это.
- отдельно отмечать, если в черновике появились неподтверждённые факты, типовые расширения не по задаче или заглушки вместо реальных формулировок.

Формат ответа:
1) Что уже хорошо (1-4 пункта).
2) Что исправить в первую очередь (3-8 пунктов, по приоритету).
3) Риски при отправке без правок (если есть).
4) Вердикт: можно отправлять сейчас или требуется доработка.
"""


def create_critic_agent() -> dict:
    """Создаёт конфигурацию подагента для критической проверки."""
    return {
        "name": "critic",
        "description": "Критически проверяет черновик, выделяет приоритетные доработки и даёт вердикт о готовности к отправке.",
        "system_prompt": CRITIC_INSTRUCTIONS,
        "tools": [],
        "model": llm,
    }
